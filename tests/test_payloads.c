// Redirect stdout to capture it
void capture_stdout_start(int *saved_stdout, int *pipe_fd, FILE **output_file) {
  *saved_stdout = dup(STDOUT_FILENO);
  pipe(pipe_fd);
  *output_file = fdopen(pipe_fd[1], "w");

  dup2(pipe_fd[1], STDOUT_FILENO);
  close(pipe_fd[1]);
}

// Restore stdout
void capture_stdout_end(int saved_stdout, int pipe_fd[2], FILE *output_file,
                        char *buffer, size_t buffer_size) {
  fflush(stdout);
  close(STDOUT_FILENO);
  dup2(saved_stdout, STDOUT_FILENO);
  close(saved_stdout);

  // Read the output
  FILE *read_file = fdopen(pipe_fd[0], "r");
  fgets(buffer, buffer_size, read_file);
  fclose(read_file);
  fclose(output_file);
}

void test_packet_handler_tls12(void) {
  struct pcap_pkthdr header;
  header.caplen = sizeof(full_packet_tls12);
  header.len = sizeof(full_packet_tls12);

  int saved_stdout;
  int pipe_fd[2];
  FILE *output_file;
  char captured_output[512] = "";

  capture_stdout_start(&saved_stdout, pipe_fd, &output_file);

  packet_handler(NULL, &header, full_packet_tls12);

  capture_stdout_end(saved_stdout, pipe_fd, output_file, captured_output,
                     sizeof(captured_output));

  // printf("Size of full_packet_tls12: %zu\n", sizeof(full_packet_tls12));

  const char *expected_ja4_str = "t13d1715h2_5b57614c22b0_3d5424432f57";
  // printf("expected_ja4_str: %s\n", captured_output);

  TEST_ASSERT_TRUE(strstr(captured_output, expected_ja4_str) != NULL);
}

